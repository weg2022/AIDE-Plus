	/**
	 * Reports an error that occurred while scanning.
	 *
	 * <p>In a well-formed scanner (no or only correct usage of {@code yypushback(int)} and a
	 * match-all fallback rule) this method will only be called with things that
	 * "Can't Possibly Happen".
	 *
	 * <p>If this method is called, something is seriously wrong (e.g. a JFlex bug producing a faulty
	 * scanner etc.).
	 *
	 * <p>Usual syntax/scanner level error handling should be done in error fallback rules.
	 *
	 * @param errorCode the code of the error message to display.
	 */
	private static void zzScanError( int errorCode ) {
		String message;
		try {
			message = ZZ_ERROR_MSG[errorCode];
		}
		catch (ArrayIndexOutOfBoundsException e) {
			message = ZZ_ERROR_MSG[ZZ_UNKNOWN_ERROR];
		}

		throw new Error(message);
	}


	/**
	 * Pushes the specified amount of characters back into the input stream.
	 *
	 * <p>They will be read again by then next call of the scanning method.
	 *
	 * @param number the number of characters to be read again. This number must not be greater than
	 *     {@link #yylength()}.
	 */
	public void yypushback( int number )  {
		if ( number > yylength() )
			zzScanError(ZZ_PUSHBACK_2BIG);

		zzMarkedPos -= number;
	}




	/**
	 * Resumes scanning until the next regular expression is matched, the end of input is encountered
	 * or an I/O-Error occurs.
	 *
	 * @return the next token.
	 * @exception java.io.IOException if any I/O-Error occurs.
	 */
	public int yylex( ) throws java.io.IOException {
		int zzInput;
		int zzAction;

		// cached fields:
		int zzCurrentPosL;
		int zzMarkedPosL;
		int zzEndReadL = zzEndRead;
		char[] zzBufferL = zzBuffer;

		int [] zzTransL = ZZ_TRANS;
		int [] zzRowMapL = ZZ_ROWMAP;
		int [] zzAttrL = ZZ_ATTRIBUTE;

		while ( true ) {
			zzMarkedPosL = zzMarkedPos;

			yychar += zzMarkedPosL - zzStartRead;

			boolean zzR = false;
			int zzCh;
			int zzCharCount;
			for ( zzCurrentPosL = zzStartRead  ;
				 zzCurrentPosL < zzMarkedPosL ;
			zzCurrentPosL += zzCharCount ) {
				zzCh = Character.codePointAt(zzBufferL, zzCurrentPosL, zzMarkedPosL);
				zzCharCount = Character.charCount(zzCh);
				switch ( zzCh ) {
					case '\u000B':  // fall through
					case '\u000C':  // fall through
					case '\u0085':  // fall through
					case '\u2028':  // fall through
					case '\u2029':
						yyline++;
						yycolumn = 0;
						zzR = false;
						break;
					case '\r':
						yyline++;
						yycolumn = 0;
						zzR = true;
						break;
					case '\n':
						if ( zzR )
							zzR = false;
						else {
							yyline++;
							yycolumn = 0;
						}
						break;
					default:
						zzR = false;
						yycolumn += zzCharCount;
				}
			}

			if ( zzR ) {
				// peek one character ahead if it is
				// (if we have counted one line too much)
				boolean zzPeek;
				if ( zzMarkedPosL < zzEndReadL )
					zzPeek = zzBufferL[zzMarkedPosL] == '\n';
				else if ( zzAtEOF )
					zzPeek = false;
				else {
					boolean eof = zzRefill();
					zzEndReadL = zzEndRead;
					zzMarkedPosL = zzMarkedPos;
					zzBufferL = zzBuffer;
					if ( eof )
						zzPeek = false;
					else
						zzPeek = zzBufferL[zzMarkedPosL] == '\n';
				}
				if ( zzPeek ) yyline--;
			}
			zzAction = -1;

			zzCurrentPosL = zzCurrentPos = zzStartRead = zzMarkedPosL;

			zzState = ZZ_LEXSTATE[zzLexicalState];

			// set up zzAction for empty match case:
			int zzAttributes = zzAttrL[zzState];
			if ( ( zzAttributes & 1 ) == 1 ) {
				zzAction = zzState;
			}


			zzForAction: {
				while ( true ) {

					if ( zzCurrentPosL < zzEndReadL ) {
						zzInput = Character.codePointAt(zzBufferL, zzCurrentPosL, zzEndReadL);
						zzCurrentPosL += Character.charCount(zzInput);
					} else if ( zzAtEOF ) {
						zzInput = YYEOF;
						break zzForAction;
					} else {
						// store back cached positions
						zzCurrentPos  = zzCurrentPosL;
						zzMarkedPos   = zzMarkedPosL;
						boolean eof = zzRefill();
						// get translated positions and possibly new buffer
						zzCurrentPosL  = zzCurrentPos;
						zzMarkedPosL   = zzMarkedPos;
						zzBufferL      = zzBuffer;
						zzEndReadL     = zzEndRead;
						if ( eof ) {
							zzInput = YYEOF;
							break zzForAction;
						} else {
							zzInput = Character.codePointAt(zzBufferL, zzCurrentPosL, zzEndReadL);
							zzCurrentPosL += Character.charCount(zzInput);
						}
					}
					int zzNext = zzTransL[zzRowMapL[zzState] + zzCMap(zzInput)];
					if ( zzNext == -1 ) break zzForAction;
					zzState = zzNext;

					zzAttributes = zzAttrL[zzState];
					if ( ( zzAttributes & 1 ) == 1 ) {
						zzAction = zzState;
						zzMarkedPosL = zzCurrentPosL;
						if ( ( zzAttributes & 8 ) == 8 ) break zzForAction;
					}

				}
			}

			// store back cached position
			zzMarkedPos = zzMarkedPosL;

			if ( zzInput == YYEOF && zzStartRead == zzCurrentPos ) {
				zzAtEOF = true;
				switch ( zzLexicalState ) {
					case YYINITIAL: {
							return YYEOF;
						}  // fall though
					case 979: break;
					case PARAM_LIST_OR_ID: {
							yybegin(YYINITIAL);
						}  // fall though
					case 980: break;
					case PARAM_LIST: {
							yybegin(YYINITIAL);
						}  // fall though
					case 981: break;
					case ARRAY_DESCRIPTOR: {
							yybegin(YYINITIAL);
						}  // fall though
					case 982: break;
					case STRING: {
							return invalidStateBasedToken("Unterminated string literal");
						}  // fall though
					case 983: break;
					case CHAR: {
							return invalidStateBasedToken("Unterminated character literal");
						}  // fall though
					case 984: break;
					default:
						{
							return YYEOF;
						}
				}
			} else {
				switch ( zzAction < 0 ? zzAction : ZZ_ACTION[zzAction] ) {
					case 1:
						{ return PlainStyle;
						}
						// fall through
					case 43: break;
					case 2:
						{ beginStateBasedToken(STRING); sb.append('"'); return LiteralStyle;
						}
						// fall through
					case 44: break;
					case 3:
						{ return CommentStyle;
						}
						// fall through
					case 45: break;
					case 4:
						{ return IdentifierStyle;
						}
						// fall through
					case 46: break;
					case 5:
						{ beginStateBasedToken(CHAR); sb.append('\'');  return LiteralStyle;
						}
						// fall through
					case 47: break;
					case 6:
						{ return SeparatorStyle;
						}
						// fall through
					case 48: break;
					case 7:
						{ return LiteralStyle;
						}
						// fall through
					case 49: break;
					case 8:
						{ return OperatorStyle;
						}
						// fall through
					case 50: break;
					case 9:
						{ return TypeStyle;
						}
						// fall through
					case 51: break;
					case 10:
						{ yybegin(ARRAY_DESCRIPTOR);
							return TypeStyle;
						}
						// fall through
					case 52: break;
					case 11:
						{ yypushback(1); yybegin(YYINITIAL);
						}
						// fall through
					case 53: break;
					case 12:
						{ yybegin(YYINITIAL); return TypeStyle;
						}
						// fall through
					case 54: break;
					case 13:
						{ sb.append(yytext());
						}
						// fall through
					case 55: break;
					case 14:
						{ return invalidStateBasedToken("Unterminated string literal");
						}
						// fall through
					case 56: break;
					case 15:
						{ sb.append('"'); return endStateBasedToken(LiteralStyle);
						}
						// fall through
					case 57: break;
					case 16:
						{ return invalidStateBasedToken("Unterminated character literal");
						}
						// fall through
					case 58: break;
					case 17:
						{ sb.append('\'');
							if ( sb.length() == 2 ) {
								return invalidStateBasedToken("Empty character literal");
							} else if ( sb.length() > 3 ) {
								return invalidStateBasedToken("Character literal with multiple chars");
							}

							return endStateBasedToken(LiteralStyle);
						}
						// fall through
					case 59: break;
					case 18:
						{ sb.append(yytext());
							return endStateBasedToken(NamespaceStyle);
						}
						// fall through
					case 60: break;
					case 19:
						{ // go back and re-lex it as a PARAM_LIST_OR_ID
							yypushback(yylength());
							yybegin(PARAM_LIST_OR_ID);
						}
						// fall through
					case 61: break;
					case 20:
						{ return KeywordStyle;
						}
						// fall through
					case 62: break;
					case 21:
						{ sb.append(yytext());
							setStateBasedTokenError("Invalid escape sequence " + yytext());
						}
						// fall through
					case 63: break;
					case 22:
						{ sb.append('"');
						}
						// fall through
					case 64: break;
					case 23:
						{ sb.append('\'');
						}
						// fall through
					case 65: break;
					case 24:
						{ sb.append('\\');
						}
						// fall through
					case 66: break;
					case 25:
						{ sb.append('\b');
						}
						// fall through
					case 67: break;
					case 26:
						{ sb.append('\f');
						}
						// fall through
					case 68: break;
					case 27:
						{ sb.append('\n');
						}
						// fall through
					case 69: break;
					case 28:
						{ sb.append('\r');
						}
						// fall through
					case 70: break;
					case 29:
						{ sb.append('\t');
						}
						// fall through
					case 71: break;
					case 30:
						{ sb.append(yytext());
							setStateBasedTokenError("Invalid \\u sequence. \\u must be followed by 4 hex digits");
						}
						// fall through
					case 72: break;
					case 31:
						{ sb.append(yytext());
							setStateBasedTokenError("Invalid \\u sequence. \\u must be followed by exactly 4 hex digits");
						}
						// fall through
					case 73: break;
					case 32:
						{ sb.append(yytext());
							yybegin(CLASS_DESCRIPTOR_REMAINING);
							return NamespaceStyle;
						}
						// fall through
					case 74: break;
					case 33:
						{ sb.append(yytext());
							return NamespaceStyle;
						}
						// fall through
					case 75: break;
					case 34:
						{ // go back and re-lex it as a PARAM_LIST
							yypushback(yylength());
							yybegin(PARAM_LIST);
						}
						// fall through
					case 76: break;
					case 35:
						{ yypushback(yylength());
							beginStateBasedToken(CLASS_DESCRIPTOR_BEGINNING);
						}
						// fall through
					case 77: break;
					case 36:
						{ yypushback(yylength());
							beginStateBasedToken(CLASS_DESCRIPTOR_BEGINNING);
							sb.append(yytext());
						}
						// fall through
					case 78: break;
					case 37:
						{ return DelegateStyle;
						}
						// fall through
					case 79: break;
					case 38:
						{ sb.append("L");
							sb.append(processQuotedSimpleNameWithSpaces(yytext().substring(1)));
							yybegin(CLASS_DESCRIPTOR_REMAINING);
							return NamespaceStyle;
						}
						// fall through
					case 80: break;
					case 39:
						{ sb.append("L");
							sb.append(processQuotedSimpleName(yytext().substring(1)));
							yybegin(CLASS_DESCRIPTOR_REMAINING);
							return NamespaceStyle;
						}
						// fall through
					case 81: break;
					case 40:
						{ sb.append("/");
							sb.append(processQuotedSimpleNameWithSpaces(yytext().substring(1)));
							return NamespaceStyle;
						}
						// fall through
					case 82: break;
					case 41:
						{ sb.append("/");
							sb.append(processQuotedSimpleName(yytext().substring(1)));
							return NamespaceStyle;
						}
						// fall through
					case 83: break;
					case 42:
						{ sb.append((char)Integer.parseInt(yytext().substring(2, 6), 16));
						}
						// fall through
					case 84: break;
					default:
						zzScanError(ZZ_NO_MATCH);
				}
			}
		}
	}


